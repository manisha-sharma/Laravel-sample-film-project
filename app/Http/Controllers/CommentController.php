     */
    public function store(Request $request)
    {
        //
		$this->validate($request, [
			'name' => 'required',
			'comments' => 'required',
		]);
		$input = $request->all();
		if($input['user_id'] == 0){
			Session::flash('flash_message', 'Please login in order to add comment!');
		}else{
			Comment::create($input);
			Session::flash('flash_message', 'Comment successfully added!');
		}
		//return view('backend/films.view')->withlist($film_list);
		return redirect()->back();
    }

